package tp7;

public class DDJJFertilizanteIngresoDetalle {
    
    private String codigoFertilizante; 
    private String nombreFertilizante; 
    private String tipoEnvase; 
    private double capacidadEnvase; 
    private int cantidadEnvases; 
    private double subtotal; 

    public String getCodigoFertilizante() {
        return codigoFertilizante;
    }

    public void setCodigoFertilizante(String codigoFertilizante) {
        this.codigoFertilizante = codigoFertilizante;
    }

    public String getNombreFertilizante() {
        return nombreFertilizante;
    }

    public void setNombreFertilizante(String nombreFertilizante) {
        this.nombreFertilizante = nombreFertilizante;
    }

    public String getTipoEnvase() {
        return tipoEnvase;
    }

    public void setTipoEnvase(String tipoEnvase) {
        this.tipoEnvase = tipoEnvase;
    }

    public double getCapacidadEnvase() {
        return capacidadEnvase;
    }

    public void setCapacidadEnvase(double capacidadEnvase) {
        this.capacidadEnvase = capacidadEnvase;
    }

    public int getCantidadEnvases() {
        return cantidadEnvases;
    }

    public void setCantidadEnvases(int cantidadEnvases) {
        this.cantidadEnvases = cantidadEnvases;
    }

    public double getSubtotal() {
        return subtotal;
    }

    public void setSubtotal(double subtotal) {
        this.subtotal = subtotal;
    }
    
    
    
}
